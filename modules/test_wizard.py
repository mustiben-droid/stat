import streamlit as st
import pandas as pd

def render_test_wizard(df: pd.DataFrame):
    st.subheader("🧙 אשף בחירת המבחן הסטטיסטי")
    st.write("לא בטוח באיזה מבחן להשתמש? ענה על 2 שאלות ונגלה לך.")

    col1, col2 = st.columns(2)

    with col1:
        st.markdown("### 1. מה סוג המשתנה התלוי (DV)?")
        dv_type = st.radio(
            "המשתנה שאותו רוצים להסביר:",
            ["מספרי (Scale) - למשל ציון, גיל, זמן", 
             "קטגוריאלי (Nominal) - למשל קבוצה, מגדר, כן/לא"]
        )

    with col2:
        st.markdown("### 2. מה סוג המשתנה הבלתי תלוי (IV)?")
        iv_type = st.radio(
            "המשתנה המסביר / המנבא:",
            ["מספרי (Scale)", "קטגוריאלי (2 קבוצות בלבד)", "קטגוריאלי (3 קבוצות ומעלה)"]
        )

    st.divider()

    # לוגיקת ההמלצה
    st.markdown("### 💡 המבחן המומלץ עבורך:")
    
    if "מספרי" in dv_type:
        if iv_type == "מספרי (Scale)":
            st.success("🎯 **מתאם פירסון או רגרסיה ליניארית**")
            st.write("משתמשים במבחנים אלו כדי לבדוק קשר בין שני משתנים כמותיים.")
        elif iv_type == "קטגוריאלי (2 קבוצות בלבד)":
            st.success("🎯 **t-test למדגמים בלתי תלויים**")
            st.write("משתמשים במבחן זה כדי להשוות ממוצעים בין שתי קבוצות (למשל גברים מול נשים).")
        else:
            st.success("🎯 **ניתוח שונות חד-כיווני (One-Way ANOVA)**")
            st.write("משתמשים במבחן זה כדי להשוות ממוצעים בין 3 קבוצות ומעלה.")
            
    else: # DV is Nominal
        if "קטגוריאלי" in iv_type:
            st.success("🎯 **מבחן חי-בריבוע (Chi-Square)**")
            st.write("משתמשים במבחן זה כדי לבדוק קשר בין שני משתנים שמיים (למשל מגדר ועישון).")
        else:
            st.info("🎯 **רגרסיה לוגיסטית**")
            st.write("מתאים למצב שבו רוצים לנבא הסתברות לאירוע (כן/לא) באמצעות משתנה מספרי.")

    st.info("💡 ניתן להריץ את המבחנים המומלצים בלשונית 'מעבדה סטטיסטית'.")
